def add(num1, num2):
    result=num1+num2
    return result

def sub(num1, num2):
    result=num1-num2
    return result

def mul(num1, num2):
    result=num1*num2
    return result

def div(num1, num2):
    result=num1/num2
    return result

a=10
b=5
print(f"Addition is {add(a,b)}")
print(f"Subtraction is {sub(a,b)}")
print(f"Multiplication is {mul(a,b)}")
print(f"Division is {div(a,b)}")